DATA SEGMENT
STRING DB "STRING_REV","$"
STRLEN DW $-STRING
STRREV DB 20 DUP(' ')
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
MOV AX,DATA
MOV DS,AX
MOV ES,AX
MOV CX,STRLEN
ADD CX,-2
LEA SI,STRING
LEA DI,STRREV
ADD SI,STRLEN
ADD SI,-2
L1:
MOV AL,[SI]
MOV [DI],AL
INC DI
DEC SI
LOOP L1
MOV AL,[SI]
MOV [DI],AL
INC DI
MOV DL, '$'
MOV [DI],DL

LEA DX,STRREV
MOV AH,09H
INT 21H

MOV AX,4CH
INT 21H
CODE ENDS
END START
